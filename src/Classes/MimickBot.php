    /**
     * @param string $bot
     * @param string $bot2
     * @param bool $backup
     * @return array
     */
    public function fakePost($bot, $bot2 = '', $backup = false)
    {

        $message = 'mimicking '.$bot.'...';
        $this->logdata($message);

        // mixed
        if (!empty($bot2)) {
            $message = 'mixing with '.$bot2.'...';
            $this->logdata($message);

            $Mixbot = new MixBot();
            $res = $Mixbot->mix($bot, $bot2);
            if ($res['success']) {
                if (!empty($res['image'])) {
                    // backup images
                    if ($backup) {
                        try {
                            /** @var \Intervention\Image\Image $img */
                            $img = Image::make($res['image']);
                        } catch (\Intervention\Image\Exception\NotReadableException $e) {
                            $message = 'unable to instantiate image '.$res['image'];
                            $this->logdata($message, 1);
                        }

                        $path = 'C:\Users\Diego\PhpstormProjects\FakePostBot\src\debug\test\mixing\\';
                        $name = date("Y-m-d H_i_s").'['.$res['strategy'].']'.$res['method'].'.jpg';
                        $new_name = str_replace(" ", "_", strtolower($path.$name));

                        $img->save($new_name);
                        $img->destroy();
                    }
                } else {
                    $message = 'no image.';
                    $this->logdata($message);
                }

                return [
                    'image'    => $res['image'],
                    'title'    => $res['text'],
                    'bot_link' => $res['bot_links'],
                    'comment'  => $res['comments']
                ];
            } else {
                $message = 'mixing failed';
                $this->logdata($message, 1);
            }
        } else {
            $data = $this->mimick($bot);
        }

        if ($data['success']) {
            $message = 'mimicking successful.';
            $this->logdata($message);

            return [
                'image'    => $data['image'],
                'title'    => $data['title'],
                'bot_link' => $data['bot_link'],
                'comment'  => $data['comment']
            ];
        } else {
            $message = 'mimicking failed.';
            $this->logdata($message, 1);
        }
    }

